package Common;

public class ObjectStructure {
    private String id;
    private String object;

    public void setObject(String object) {
        this.object = object;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getObject() {
        return object;
    }

    public String getId() {
        return id;
    }
}
